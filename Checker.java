public class Checker{
   private int column;
   private int row;
   private String color;
   
   public Checker(){
    column = 0;
    row = 0;
    color = "none";
   }
   public Checker(int inputColumn, int inputRow, String inputColor){
      column = inputColumn;
      row = inputRow;
      color = inputColor;
   }

}